#Primeiro modo
import math

math.sqrt(25) #Calcula a raiz quadrada de 25. O resultado é 5.0.
math.log2(1024) #Calcula o logaritmo base 2 de 1024. O resultado é 10.0, porque 2 elevado a 20 é 1024.
math.cos(45) #Calcula o cosseno de 45 radianos. resultado 0.5253219888177297
#Resultado 5.0


